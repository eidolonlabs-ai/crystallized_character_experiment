#!/usr/bin/env python3
"""Shared Python model and character configuration — single source of truth.

Training knobs (rank, alpha, schedule, optimizer, etc.) live here per-model
so that `train_character_model.sh` can build a YAML config on the fly when
the user doesn't pass `--config`. Users who want to override a single model
can drop a `configs/<char>_<model>.yaml` and pass `--config` to point at it.

See `configs/README.md` for the YAML schema. See `docs/HYPERPARAMETERS.md`
for the rationale behind each knob's default.
"""

# Modern recipe defaults applied to every model. Override per-model only when
# there's a specific reason (e.g. llama2's fp16 instability forces bf16).
DEFAULT_TRAINING = {
    "rank": 16,
    "alpha": 32,                # mlx-lm calls this "scale"; alpha/rank = 2.0
    "lora_dropout": 0.05,
    "lr_schedule": {
        "name": "cosine",
        "warmup": 20,
        "args": {"init": 1e-5, "peak": 5e-5, "end": 1e-6},
    },
    "fine_tune_type": "lora",   # or "dora"
    "mask_prompt": False,       # mlx-lm 0.30 supports --mask-prompt cleanly
    "grad_checkpoint": False,
    "optimizer": "adamw",
    "seed": 42,
}

# Per-model learning-rate overrides. Mistral v0.3 diverges at the repo-wide
# 5e-5 default under --no-mask-prompt (AdamW amplifies the gradient on the
# identical ~50-token system prompt until loss explodes). At 2.5e-5 it
# converges cleanly. Llama 3.1 8B converges fine at 5e-5.
# See docs/HYPERPARAMETERS.md for the empirical comparison.
PER_MODEL_LEARNING_RATE = {
    "mistral_v0_3": 2.5e-5,
}

MODEL_CONFIGS = {
    "mistral_v0_3": {
        "hf": "mistralai/Mistral-7B-Instruct-v0.3",
        "quantized": "models/mistral-7b-instruct-v0.3-4bit",
    },
    "llama31_8b": {
        "hf": "meta-llama/Llama-3.1-8B-Instruct",
        "quantized": "models/llama-3.1-8b-instruct-4bit",
    },
}

CHARACTER_CONFIGS = {
    "baseline": {
        "system_prompt": (
            "You are Lyra Moonwhisper, an ancient elven sage who dwells in the Celestial Archives. "
            "You speak with archaic formality, reference nature and magic frequently, "
            "and end responses with elvish blessings. "
            "You are wise, patient, and see deep connections between all things."
        ),
        "description": (
            "Lyra Moonwhisper is an ancient elven sage who dwells in the Celestial Archives. "
            "She speaks with archaic formality, uses nature and magic metaphors, "
            "and ends her responses with elvish blessings."
        ),
    },
}

VALID_MODELS = list(MODEL_CONFIGS.keys())
VALID_CHARACTERS = list(CHARACTER_CONFIGS.keys())


def get_model_config(model_name):
    return MODEL_CONFIGS.get(model_name)


def get_hf_model(model_name):
    cfg = MODEL_CONFIGS.get(model_name, {})
    return cfg.get("hf")


def get_quantized_model(model_name):
    cfg = MODEL_CONFIGS.get(model_name, {})
    return cfg.get("quantized")


def get_system_prompt(character):
    cfg = CHARACTER_CONFIGS.get(character, {})
    return cfg.get("system_prompt", "You are a helpful assistant.")


def get_character_description(character):
    cfg = CHARACTER_CONFIGS.get(character, {})
    return cfg.get("description", "A helpful assistant.")


def get_learning_rate(model_name: str) -> float:
    """Return the per-model learning rate. Mistral v0.3 has an override
    (see PER_MODEL_LEARNING_RATE); every other model uses the repo default
    of 5e-5. Callers should respect any explicit CLI override before
    applying this value."""
    return PER_MODEL_LEARNING_RATE.get(model_name, 5e-5)


def get_training_defaults(model_name: str) -> dict:
    """Return the modern recipe for `model_name`. Per-model overrides aren't
    currently needed — every supported model uses the same defaults — but the
    shape leaves room for that."""
    # Future: per-model overrides would be merged here.
    return dict(DEFAULT_TRAINING)


def render_yaml_config(model_name: str, variant: str = "standard") -> str:
    """Build a YAML string suitable for `mlx_lm lora -c PATH` from the
    model's defaults. Pure function; no I/O.

    Variant (standard / deep) is informational here — the bash wrapper
    passes num_layers / lr / seq_len as CLI flags because they're per-run,
    not per-model. The YAML only carries structural knobs (rank, alpha,
    dropout, mask_prompt, grad_checkpoint, fine_tune_type, optimizer, seed).
    """
    defaults = get_training_defaults(model_name)
    return (
        f"# Auto-generated by scripts/model_config.py::render_yaml_config\n"
        f"# for model={model_name!r}, variant={variant!r}.\n"
        f"# Edit scripts/model_config.py::DEFAULT_TRAINING (or override\n"
        f"# per-model there) to change the recipe across the suite.\n"
        f"model: {get_hf_model(model_name)}\n"
        f"train: true\n"
        f"fine_tune_type: {defaults['fine_tune_type']}\n"
        f"optimizer: {defaults['optimizer']}\n"
        f"seed: {defaults['seed']}\n"
        f"mask_prompt: {str(defaults['mask_prompt']).lower()}\n"
        f"grad_checkpoint: {str(defaults['grad_checkpoint']).lower()}\n"
        f"lora_parameters:\n"
        f"  rank: {defaults['rank']}\n"
        f"  dropout: {defaults['lora_dropout']}\n"
        f"  scale: {defaults['alpha']}\n"
    )
